# ── Standard Library ──────────────────────────────────────
import json
import warnings
warnings.filterwarnings('ignore')

# ── Numerical & Data ──────────────────────────────────────
import numpy as np
import pandas as pd

# ── Visualisation ────────────────────────────────────────
import matplotlib
matplotlib.use('Agg')                          # non-interactive backend (safe for scripts)
import matplotlib.pyplot as plt
import matplotlib.gridspec as gridspec
import seaborn as sns

# ── scikit-learn ─────────────────────────────────────────
from sklearn.model_selection import (
    train_test_split,
    StratifiedKFold,
    cross_validate,
)
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import Pipeline
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    roc_auc_score,
    roc_curve,
    confusion_matrix,
    classification_report,
)

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 1 — GENERATE SYNTHETIC TELECOM CHURN DATASET
# ══════════════════════════════════════════════════════════════════════════════

np.random.seed(42)
print("=" * 60)
print("  CUSTOMER CHURN PREDICTION PIPELINE")
print("=" * 60)
print("\n[1/6]  Generating dataset …")

N = 2000   # number of customers

tenure          = np.random.exponential(24, N).clip(1, 72).astype(int)
monthly_charges = np.random.normal(65, 20, N).clip(20, 120)
total_charges   = tenure * monthly_charges + np.random.normal(0, 50, N)
num_products    = np.random.randint(1, 5, N)
support_calls   = np.random.poisson(1.5, N)
contract_type   = np.random.choice([0, 1, 2], N, p=[0.5, 0.3, 0.2])  # 0=M2M 1=1yr 2=2yr
internet_svc    = np.random.choice([0, 1], N, p=[0.3, 0.7])
senior_citizen  = np.random.choice([0, 1], N, p=[0.84, 0.16])

# Realistic churn probability: tenure & contract reduce it, support calls increase it
churn_prob = (
    0.30
    - 0.006 * tenure
    + 0.003 * monthly_charges
    + 0.040 * support_calls
    - 0.080 * contract_type
    - 0.030 * num_products
    + 0.040 * senior_citizen
    + np.random.normal(0, 0.12, N)
).clip(0.02, 0.98)

churn = (np.random.rand(N) < churn_prob).astype(int)

df = pd.DataFrame({
    'tenure':           tenure,
    'monthly_charges':  monthly_charges.round(2),
    'total_charges':    total_charges.round(2),
    'num_products':     num_products,
    'support_calls':    support_calls,
    'contract_type':    contract_type,
    'internet_service': internet_svc,
    'senior_citizen':   senior_citizen,
    'churn':            churn,
})

print(f"       Rows       : {df.shape[0]}")
print(f"       Features   : {df.shape[1] - 1}")
print(f"       Churn rate : {churn.mean():.1%}")

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 2 — DATA PREPROCESSING
# ══════════════════════════════════════════════════════════════════════════════

print("\n[2/6]  Preprocessing …")

# Verify data quality
assert df.isnull().sum().sum() == 0,  "Missing values found!"
assert df.duplicated().sum()   == 0,  "Duplicate rows found!"
print(f"       Missing values : 0  ✓")
print(f"       Duplicate rows : 0  ✓")

# Feature / target split
X = df.drop('churn', axis=1)
y = df['churn']

# Stratified 80 / 20 split — preserves the 28.8% churn ratio in both sets
X_train, X_test, y_train, y_test = train_test_split(
    X, y,
    test_size=0.20,
    random_state=42,
    stratify=y,
)
print(f"       Train samples : {X_train.shape[0]}")
print(f"       Test  samples : {X_test.shape[0]}")

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 3 — MODEL DEFINITIONS
#  Each model is a Pipeline: StandardScaler → Classifier
#  class_weight='balanced' handles the 28.8% / 71.2% class imbalance
# ══════════════════════════════════════════════════════════════════════════════

print("\n[3/6]  Defining models …")

models = {
    'Logistic Regression': Pipeline([
        ('scaler', StandardScaler()),
        ('clf',    LogisticRegression(
            max_iter=1000,
            class_weight='balanced',
            random_state=42,
        )),
    ]),

    'Random Forest': Pipeline([
        ('scaler', StandardScaler()),
        ('clf',    RandomForestClassifier(
            n_estimators=200,
            max_depth=8,
            class_weight='balanced',
            random_state=42,
        )),
    ]),

    'Gradient Boosting': Pipeline([
        ('scaler', StandardScaler()),
        ('clf',    GradientBoostingClassifier(
            n_estimators=200,
            learning_rate=0.08,
            max_depth=4,
            random_state=42,
        )),
    ]),
}

print(f"       {len(models)} models configured: {', '.join(models.keys())}")

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 4 — 5-FOLD STRATIFIED CROSS-VALIDATION (on training set only)
# ══════════════════════════════════════════════════════════════════════════════

print("\n[4/6]  Running 5-fold cross-validation …")

cv      = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)
scoring = ['accuracy', 'precision', 'recall', 'f1', 'roc_auc']

cv_results = {}
for name, pipe in models.items():
    res = cross_validate(
        pipe, X_train, y_train,
        cv=cv,
        scoring=scoring,
        return_train_score=False,
        n_jobs=-1,
    )
    cv_results[name] = {
        metric: (res[f'test_{metric}'].mean(), res[f'test_{metric}'].std())
        for metric in scoring
    }
    print(f"       {name:<22}  AUC={cv_results[name]['roc_auc'][0]:.4f} "
          f"(±{cv_results[name]['roc_auc'][1]:.4f})")

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 5 — TRAIN ON FULL TRAIN SET & EVALUATE ON HELD-OUT TEST SET
# ══════════════════════════════════════════════════════════════════════════════

print("\n[5/6]  Training & evaluating on test set …")

test_results = {}
proba_store  = {}
fitted_pipes = {}

for name, pipe in models.items():
    pipe.fit(X_train, y_train)
    fitted_pipes[name] = pipe

    y_pred  = pipe.predict(X_test)
    y_proba = pipe.predict_proba(X_test)[:, 1]
    proba_store[name] = y_proba

    test_results[name] = {
        'accuracy':  round(accuracy_score(y_test,  y_pred),          4),
        'precision': round(precision_score(y_test, y_pred),           4),
        'recall':    round(recall_score(y_test,    y_pred),           4),
        'f1':        round(f1_score(y_test,        y_pred),           4),
        'roc_auc':   round(roc_auc_score(y_test,   y_proba),          4),
    }

# ── Pretty-print results table ────────────────────────────────────────────────
print()
header = (f"{'Model':<25} {'Accuracy':>9} {'Precision':>10}"
          f" {'Recall':>8} {'F1':>8} {'ROC-AUC':>9}")
print("─" * len(header))
print(header)
print("─" * len(header))
for name, r in test_results.items():
    print(f"{name:<25} {r['accuracy']:>9.4f} {r['precision']:>10.4f}"
          f" {r['recall']:>8.4f} {r['f1']:>8.4f} {r['roc_auc']:>9.4f}")
print("─" * len(header))

best_model = max(test_results, key=lambda n: test_results[n]['roc_auc'])
print(f"\n  ★  Best model by ROC-AUC: {best_model}  "
      f"(AUC={test_results[best_model]['roc_auc']:.4f})\n")

# ── Full classification report for best model ─────────────────────────────────
print(f"  Classification Report — {best_model}")
print("─" * 50)
y_pred_best = fitted_pipes[best_model].predict(X_test)
print(classification_report(y_test, y_pred_best,
                             target_names=['No Churn', 'Churn']))

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 6 — EVALUATION PLOTS  (7 panels)
# ══════════════════════════════════════════════════════════════════════════════

print("[6/6]  Generating evaluation dashboard …")

# ── Theme colours ─────────────────────────────────────────────────────────────
DARK          = '#0d0d14'
MID           = '#16162a'
ACCENT        = '#6c63ff'
GREEN         = '#43e97b'
CORAL         = '#f97066'
GOLD          = '#ffd166'
TEAL          = '#00c9b1'
FG            = '#e8e8f0'
GRID_COLOR    = '#2a2a3d'
MODEL_COLORS  = [ACCENT, GREEN, CORAL]

plt.rcParams.update({
    'figure.facecolor': DARK,
    'axes.facecolor':   MID,
    'axes.edgecolor':   GRID_COLOR,
    'axes.labelcolor':  FG,
    'xtick.color':      FG,
    'ytick.color':      FG,
    'text.color':       FG,
    'grid.color':       GRID_COLOR,
    'grid.alpha':       0.6,
    'font.family':      'monospace',
})

metric_names  = ['accuracy', 'precision', 'recall', 'f1', 'roc_auc']
metric_labels = ['Accuracy', 'Precision', 'Recall', 'F1', 'ROC-AUC']
model_names   = list(models.keys())

fig = plt.figure(figsize=(20, 22), facecolor=DARK)
fig.suptitle(
    'CUSTOMER CHURN PREDICTION\nModel Evaluation Report',
    fontsize=18, fontweight='bold', color=FG, y=0.98, linespacing=1.6,
)

gs = gridspec.GridSpec(
    4, 3, figure=fig,
    hspace=0.52, wspace=0.38,
    top=0.93, bottom=0.04,
    left=0.07, right=0.97,
)

# ── Panel A: Grouped bar chart — CV means ± std ───────────────────────────────
ax_bar = fig.add_subplot(gs[0, :2])
x, w = np.arange(len(metric_labels)), 0.25
for i, (name, col) in enumerate(zip(model_names, MODEL_COLORS)):
    means = [cv_results[name][m][0] for m in metric_names]
    stds  = [cv_results[name][m][1] for m in metric_names]
    ax_bar.bar(
        x + (i - 1) * w, means, w,
        yerr=stds, label=name, color=col, alpha=0.88,
        error_kw=dict(ecolor=FG, capsize=3, alpha=0.6),
    )
ax_bar.set_xticks(x)
ax_bar.set_xticklabels(metric_labels, fontsize=10)
ax_bar.set_ylim(0.5, 1.0)
ax_bar.set_ylabel('Score')
ax_bar.set_title('5-Fold CV Metrics (mean ± std)', fontsize=12, pad=8)
ax_bar.legend(loc='lower right', framealpha=0.2, fontsize=8)
ax_bar.grid(axis='y')

# ── Panel B: ROC Curves ───────────────────────────────────────────────────────
ax_roc = fig.add_subplot(gs[0, 2])
for name, col in zip(model_names, MODEL_COLORS):
    fpr, tpr, _ = roc_curve(y_test, proba_store[name])
    auc = test_results[name]['roc_auc']
    ax_roc.plot(fpr, tpr, color=col, lw=2,
                label=f'{name[:10]} ({auc:.3f})')
ax_roc.plot([0, 1], [0, 1], '--', color=GRID_COLOR, lw=1)
ax_roc.set_xlabel('FPR')
ax_roc.set_ylabel('TPR')
ax_roc.set_title('ROC Curves', fontsize=12, pad=8)
ax_roc.legend(fontsize=7.5, framealpha=0.2)
ax_roc.grid(True)

# ── Panel C: Confusion Matrices (one per model) ───────────────────────────────
for i, (name, col) in enumerate(zip(model_names, MODEL_COLORS)):
    ax_cm  = fig.add_subplot(gs[1, i])
    y_pred = fitted_pipes[name].predict(X_test)
    cm     = confusion_matrix(y_test, y_pred)
    sns.heatmap(
        cm, annot=True, fmt='d', ax=ax_cm,
        cmap=sns.light_palette(col, as_cmap=True),
        linewidths=2, linecolor=DARK,
        annot_kws={'size': 14, 'weight': 'bold', 'color': DARK},
    )
    ax_cm.set_xlabel('Predicted')
    ax_cm.set_ylabel('Actual')
    ax_cm.set_title(f'{name}\nConfusion Matrix', fontsize=10, pad=6)
    ax_cm.set_xticklabels(['No Churn', 'Churn'], fontsize=8)
    ax_cm.set_yticklabels(['No Churn', 'Churn'], fontsize=8, rotation=0)

# ── Panel D: Feature Importance — Random Forest & Gradient Boosting ──────────
feat_names = X.columns.tolist()
for idx, (mname, col) in enumerate([
    ('Random Forest',    GREEN),
    ('Gradient Boosting', CORAL),
]):
    ax_fi = fig.add_subplot(gs[2, idx])
    clf   = fitted_pipes[mname].named_steps['clf']
    imp   = pd.Series(clf.feature_importances_, index=feat_names).sort_values()
    bar_colors = [col if v == imp.max() else '#3a3a5c' for v in imp.values]
    imp.plot(kind='barh', ax=ax_fi, color=bar_colors, edgecolor='none')
    ax_fi.set_title(f'{mname}\nFeature Importance', fontsize=10, pad=6)
    ax_fi.set_xlabel('Importance')
    ax_fi.grid(axis='x', alpha=0.4)

# ── Panel E: Logistic Regression |Coefficients| ───────────────────────────────
ax_coef  = fig.add_subplot(gs[2, 2])
lr_clf   = fitted_pipes['Logistic Regression'].named_steps['clf']
coefs    = pd.Series(np.abs(lr_clf.coef_[0]), index=feat_names).sort_values()
bar_cols = [ACCENT if v == coefs.max() else '#3a3a5c' for v in coefs.values]
coefs.plot(kind='barh', ax=ax_coef, color=bar_cols, edgecolor='none')
ax_coef.set_title('Logistic Regression\n|Coefficient| Magnitude', fontsize=10, pad=6)
ax_coef.set_xlabel('|Coefficient|')
ax_coef.grid(axis='x', alpha=0.4)

# ── Panel F: Test-Set Score Heatmap ──────────────────────────────────────────
ax_heat   = fig.add_subplot(gs[3, :2])
heat_data = pd.DataFrame(
    {n: test_results[n] for n in model_names},
    index=metric_labels,
).T
sns.heatmap(
    heat_data, annot=True, fmt='.4f', ax=ax_heat,
    cmap='RdYlGn', vmin=0.5, vmax=1.0,
    linewidths=1.5, linecolor=DARK,
    annot_kws={'size': 10, 'weight': 'bold', 'color': DARK},
    cbar_kws={'shrink': 0.8},
)
ax_heat.set_title('Test Set Score Heatmap', fontsize=12, pad=8)
ax_heat.tick_params(axis='x', rotation=30)
ax_heat.tick_params(axis='y', rotation=0)

# ── Panel G: Predicted Probability Distribution (best model) ─────────────────
ax_dist    = fig.add_subplot(gs[3, 2])
proba_best = proba_store[best_model]
ax_dist.hist(proba_best[y_test == 0], bins=30, alpha=0.7,
             color=TEAL,  label='No Churn', density=True)
ax_dist.hist(proba_best[y_test == 1], bins=30, alpha=0.7,
             color=CORAL, label='Churn',    density=True)
ax_dist.axvline(0.5, color=GOLD, ls='--', lw=1.5, label='Threshold=0.5')
ax_dist.set_title(f'{best_model}\nPredicted Probability Dist.', fontsize=10, pad=6)
ax_dist.set_xlabel('P(Churn)')
ax_dist.set_ylabel('Density')
ax_dist.legend(fontsize=8, framealpha=0.2)
ax_dist.grid(axis='y', alpha=0.4)

# ── Save figure ───────────────────────────────────────────────────────────────
PLOT_PATH = 'churn_evaluation.png'
plt.savefig(PLOT_PATH, dpi=150, bbox_inches='tight', facecolor=DARK)
plt.close()
print(f"       Plot saved   → {PLOT_PATH}")

# ══════════════════════════════════════════════════════════════════════════════
#  SECTION 7 — EXPORT METRICS TO JSON
# ══════════════════════════════════════════════════════════════════════════════

summary = {
    'dataset_info': {
        'n_samples':   int(N),
        'n_features':  int(X.shape[1]),
        'churn_rate':  round(float(y.mean()), 4),
        'train_size':  int(X_train.shape[0]),
        'test_size':   int(X_test.shape[0]),
    },
    'test_results': {
        name: {k: round(v, 4) for k, v in r.items()}
        for name, r in test_results.items()
    },
    'cv_results': {
        name: {
            metric: {'mean': round(vals[0], 4), 'std': round(vals[1], 4)}
            for metric, vals in r.items()
        }
        for name, r in cv_results.items()
    },
    'best_model': best_model,
}

JSON_PATH = 'metrics.json'
with open(JSON_PATH, 'w') as f:
    json.dump(summary, f, indent=2)
print(f"       Metrics saved → {JSON_PATH}")

print("\n" + "=" * 60)
print(f"  ✓  Pipeline complete.")
print(f"  ✓  Best model  : {best_model}")
print(f"  ✓  ROC-AUC     : {test_results[best_model]['roc_auc']:.4f}")
print(f"  ✓  Recall      : {test_results[best_model]['recall']:.4f}")
print("=" * 60)